package control_statement;

public class SwitchStudyMONTH {

	public static void main(String[] args) {
		
		String Month="Feb";
		
		switch (Month) {
		case "Jan":System.out.println("This is 1st month of year");
		break;
		case "Feb":System.out.println("This is 2nd month of year");
		break;
		case "March":System.out.println("This is 3rd month of year");
		break;
		case "April":System.out.println("This is 4th month of year");
		break;
		case "May":System.out.println("This is 5th month of year");
		break;
		case "Jun":System.out.println("This is 6th month of year");
		break;
		case "July":System.out.println("This is 7th month of year");
		break;
		case "Aug":System.out.println("This is 8th month of year");
		break;
		case "Sep":System.out.println("This is 9th month of year");
		break;
		case "Oct":System.out.println("This is 10th month of year");
		break;
		case "Nov":System.out.println("This is 11th month of year");
		break;
		case "Dec":System.out.println("This is 12th month of year");
		break;

		default:System.out.println("Plz enter the Valid Month From Jan to Dec");
			break;
		}
		
		}
		
	}

