package loop;

public class DoWhileLoopStudy {

	public static void main(String[] args) {
		
		
		// print 1to 10 numbers
		 int i=1;
		do {
			System.out.println(i);
			i++;
			
			
		} 
		
		while (i<=10);
		
		//print 2 table 
		
		int j=2;
		do {
			System.out.println(j);
			j=j+2;
			
		} while (j<=20);
		
		
		}
	

}
